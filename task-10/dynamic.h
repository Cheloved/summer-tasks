/**
 * @file dynamic.h
 * @brief Заголовочный файл для работы с динамическими двумерными массивами строк.
 *
 * Содержит объявления функций для:
 * - выделения памяти под двумерный массив (`init_char_arr`),
 * - освобождения выделенной памяти (`free_char_arr`).
 *
 * @author Ракитин Илья Алексеевич
 */

#pragma once

#include <stdio.h>
#include <stdlib.h>

/**
 * @brief Выделяет память под двумерный массив строк.
 *
 * Создаёт массив указателей на строки (`char**`) размером `sizex`,
 * каждая из которых имеет длину `sizey`.
 *
 * @param sizex Количество строк (размер по X).
 * @param sizey Длина каждой строки (размер по Y).
 * @return Указатель на выделенный двумерный массив, или NULL в случае ошибки.
 */
char** init_char_arr(int sizex, int sizey);

/**
 * @brief Освобождает память, выделенную под двумерный массив строк.
 *
 * Последовательно освобождает каждую строку, а затем и сам массив указателей.
 *
 * @param ar Указатель на массив, который нужно освободить.
 * @param sizex Количество строк в массиве.
 * @return Возвращает 0 при успешном завершении.
 */
int free_char_arr(char** ar, int sizex);
